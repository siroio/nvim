      -- Helper function for buttons to match nvimdots style
      local function button(sc, txt, keybind, keybind_opts)
        local opts = {
          position = "center",
          shortcut = sc,
          cursor = 5,
          width = 50,
          align_shortcut = "right",
          hl_shortcut = "AlphaShortcut",
        }
        if keybind then
          keybind_opts = vim.F.if_nil(keybind_opts, { noremap = true, silent = true, nowait = true })
          opts.keymap = { "n", sc, keybind, keybind_opts }
        end
        return {
          type = "button",
          val = txt,
          on_press = function()
            -- Just feed the key, assuming it's mapped or we map it directly
            local key = vim.api.nvim_replace_termcodes(keybind or sc .. "<Ignore>", true, false, true)
            vim.api.nvim_feedkeys(key, "t", false)
          end,
          opts = opts,
        }
      end

      -- Menu Items (English, matching nvimdots text/icons)
